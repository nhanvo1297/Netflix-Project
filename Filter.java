import java.util.*;
import java.io.*;

public class Filter {
	private String operator;
	private String target;
	
	public Filter(String operator, String target) {
		this.operator=operator;
		this.target=target;
	}
	
	public String getOperator() {
		return operator;
	}
	public String getTarget() {
		return target;
	}

	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
